package main

import (
	"fmt"
	"os"
	"os/exec"
	"strings"
	"text/template"
	"time"
)

func main() {
	fmt.Println("gen.go")

	repo, err := exec.Command("git", "rev-parse", "HEAD").Output()
	if err != nil {
		panic(err)
	}
	version := fmt.Sprintf("v0.1.0-r%s", repo)

	f, err := os.Create("version/version.go")
	if err != nil {
		panic(err)
	}
	defer f.Close()

	fmt.Println("[gen] version/version.go")
	versionTmpl.Execute(f, struct {
		Timestamp time.Time
		Version   string
	}{
		Timestamp: time.Now(),
		Version:   strings.TrimSpace(string(version)),
	})
}

var versionTmpl = template.Must(template.New("").Parse(`// Code generated by go generate; DO NOT EDIT.
// {{ .Timestamp }}
package version

var String = "{{ .Version }}"
`))
